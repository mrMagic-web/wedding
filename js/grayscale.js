$(document).ready(function(){
  var $win = $(window);
  var winH = $win.height(); 
  
  $(window).scroll(function(){
    var scroll = $(window).scrollTop();
    if (scroll > winH/2) {
      $(".navbar").addClass("stay-top");   
    }

    else{
      $(".navbar").removeClass("stay-top");
    }
  })
})